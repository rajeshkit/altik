public class EmiDetails {
    int amount;
    String customerName;
    float rateOfInterest;
    int loanTenure;

    public EmiDetails(int amount, String customerName, float rateOfInterest, int loanTenure) {
        this.amount = amount;
        this.customerName = customerName;
        this.rateOfInterest = rateOfInterest;
        this.loanTenure = loanTenure;
    }
}
